"""アプローチ解説画面を担当するモジュール"""

import streamlit as st


def create_approach_view():
    """アプローチ解説ビューを作成"""
    st.title("アプローチ解説")
    st.markdown("""
    ### サマリー
    - **電源構成の影響評価**: 原子力・太陽光の発電量変化による価格変動を、エリア別に定量的に評価
    - **需給運用のモデル化**: 余剰発電を他エリアへの連系線活用、揚水発電での夜間活用など実運用を考慮
    - **複数シナリオでの分析**: 燃料価格・為替・電源構成を組み合わせた2045年までの価格推移検証

    ### 計算の仕組み

    #### 1. 電源構成・需給調整
    - **エリア別影響係数**: エリアごとの電源構成と価格の相関から算出（価格影響分析参照）
    - **余剰電力の活用**: 連系線 → 火力抑制 → 揚水 → 出力制御の優先順で調整

    #### 2. 価格への影響反映
    - **電源構成**: 原子力・太陽光発電の構成比変化を反映
    - **燃料価格**: IEA WEO・EIAシナリオに基づくLNG価格想定
    - **為替**: ITI-JIDEAモデルでの2035年想定レート（135円）を反映

    ### 前提条件
    - **電源シナリオ**: 2024年現状または7次エネ基を反映
    - **需要想定**: 分析全体を構成比で実施するため、需要増分を価格増減影響から控除　（実装予定）
    - **価格影響**: エリア別の過去実績から算出した影響係数を使用
    """)
